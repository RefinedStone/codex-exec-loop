    let haystack_bytes = haystack.as_bytes();
    // 학습 주석: `let`은 새 지역 변수를 만들며, `mut`가 있을 때만 이후에 값을 다시 대입할 수 있습니다.
    let needle_bytes = needle.as_bytes();
