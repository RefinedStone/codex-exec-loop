    // 학습 주석: 반복문은 컬렉션이나 조건을 기준으로 같은 처리를 여러 번 수행할 때 사용합니다.
    for task in proposed_tasks {
        lines.push(format!("- {}", queue_task_line(&task)));
        lines.push(format!("  rank_reasons={}", task.rank_reasons.join(" | ")));
    }
